/*
■レクチャー81
絶対値の取得「ABS」

▼絶対値とは
数値の符号を考えない、ゼロからの距離の大きさを表す数値

・10の絶対値は10
・-10の絶対値は10
・0の絶対値は0
*/

/*10の絶対値*/
SELECT ABS(10);

/*-10の絶対値*/
SELECT ABS(-10);

/*0の絶対値*/
SELECT ABS(0);

